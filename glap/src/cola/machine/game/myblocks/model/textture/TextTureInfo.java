package cola.machine.game.myblocks.model.textture;

public class TextTureInfo {
	public float minX=0;
	public float minY=0;
	public float maxX=0;
	public float maxY=0;
	public TextTureInfo(float minX,float minY,float maxX,float maxY){
		this.minX=minX;
		this.minY=minY;
		this.maxX=maxX;
		this.maxY=maxY;
	}
}
